        //coge la posicion del pokemo que quiere pintar 
    private void dibujaElPokemonQueEstaEnLaPosicion (int posicion){
        int fila = posicion / 31;
        int columna = posicion % 31;
        Graphics2D g2 = (Graphics2D) buffer.getGraphics();
        //borro lo que hubiera
        g2.setColor(Color.black);
        g2.fillRect(0, 0, alto, ancho);
        g2.drawImage(imagenPokemons,
                0,
                0,
                ancho,
                alto,
                96*columna,
                96*fila,
                96*columna + 96,
                96*fila + 96,
                null);
        repaint();
        escribeDatos();
    }
        //escribe los datos que obtengo de la consulta y los pinta en varios jlabel
    private void escribeDatos(){
        Pokemon p = listaPokemons.get(String.valueOf(contador+1));
        if (p != null){
            jLabel1.setText(p.nombre);
            especie.setText(p.species);
            terreno.setText(p.habitat);
            color.setText(p.color);
            altura.setText(p.height);
            anchura.setText(p.weight);
            
        }
        //en caso de no tener datos de ese pokemon en la base de datos devuelve lo siguiente
        else {
            jLabel1.setText("EXTINGUIDO CUAL KABUTO");
            especie.setText("NO SE SABE");
            terreno.setText("PEDREGOSO");
            color.setText("MARRON CHARCA");
            altura.setText("MESSI");
            anchura.setText("DE MAS");
        }
    }
